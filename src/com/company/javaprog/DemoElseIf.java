package com.company.javaprog;

//Else if Program

public class DemoElseIf {
        public static void main(String args[]) {
            int a = 5, b = 5;

            if (b > a) {
                System.out.println("b is greater");
            }
            else if(a > b){
                System.out.println("a is greater");
            }
            else {
                System.out.println("Both are equal");
            }
        }
    }

